package OPERATORS;


public class UnaryOperator {

	public static void main(String[] args) {
		int x=10;
		int y=++x;
		System.out.println(y);
		
		int r=10;
		int n=r++;
		System.out.println(n);
		
		int q=10;
		int d=--q;
		System.out.println(d);
		
		int m=10;
		int z=m--;
		System.out.println(z);
		
	}

}
