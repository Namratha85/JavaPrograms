package OPERATORS;
// >,<,>=,<=,!=,==
public class RelationalOperator {
public static void main(String[] args) {
	int a=20;
	int b=30;
	if (a>b) {
		System.out.println("a is greater than b "+a);
	}else
	{
		System.out.println("b is greater than a "+b);
	}
	
	int s=20;
	int v=30;
	if (s>=v) {
		System.out.println("s is greater than v "+s);
	}else
	{
		System.out.println("v is greater than s "+v);
	}
}
}
