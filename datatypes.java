package ecom.edu;

public class datatypes {

	public static void main(String[] args) {
		int i=10;
		float f=10.10f;
		long l=12222l;
		double d=12.29898d;
		System.out.println("datatype float"+f);
		System.out.println("datatype int"+i);
		System.out.println("datatype long"+l);
		System.out.println("datatype double"+d);
		
	}

}
