package OPERATORS;
//a+b
//-,+,*,%,/

public class ArithmetiOperator {

	public static void main(String[] args) {
		int a=100;
		int b=20;
		int c=a+b;
		System.out.println("addition of two numbers"+c);
		
		int e=100;
		int f=20;
		int g=e-f;
		System.out.println("subtraction of two numbers"+g);
		
		int r=100;
		int n=20;
		int p=r*n;
		System.out.println("multiplication of two numbers"+p);
		
		int s=100;
		int j=20;
		int l=s/j;
		System.out.println("Division of two numbers"+l);
		
		int k=625;
		int v=5;
		int q=k%v;
		System.out.println("percentage of two numbers"+q);
	}
}
