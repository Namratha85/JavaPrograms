package polymerphsim.edu;

public class OperatorOverloading {
	int a=1;
	int b=2;

	public static void main(String[] args) {
		OperatorOverloading ob=new OperatorOverloading();
		System.out.println("a+b concatenation "+ob.a+ob.b);
		System.out.println("(a+b) addition"+(ob.a+ob.b));
	}

}
